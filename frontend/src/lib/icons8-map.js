export function resolveIconKey(name) {
  if (!name) return "warning";
  return ICONS8_ICON_MAP[name] ? name : ICONS8_LEGACY_MAP[name] || name;
}

export function resolveIcons8Name(name) {
  const key = resolveIconKey(name);
  return ICONS8_ICON_MAP[key] || key;
}
